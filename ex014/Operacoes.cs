namespace Math{
    public class Operacoes{
        public static int Adicao(int a, int b){
            return a + b;
        }
        public static int Subtracao(int a, int b){
            return a - b;
        }
        public static int Divisao(int a, int b){
            return a / b;
        }
        public static int Multiplicacao(int a, int b){
            return a * b;
        }
    }
}